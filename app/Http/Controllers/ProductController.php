<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use \DB;
Use App\Product;
use App\Http\Requests;
use App\Http\Controllers\Controller;

class ProductController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $products = Product::all();
        return view('product.view_products',compact('products'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $categories = DB::table('category')->distinct()->get();
        return view('product.add',compact('categories'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'product_name' => 'required|max:255',
            'product_total_quantity' => 'required',
            'product_sku' => 'required|max:255',
            'price' => 'required',
            'category_id' => 'required|exists:category',
            'img_url' => 'required',
        ]);

        Product::create($request->all());

        return redirect()->back()->with('message', 'New Product Successfully Created');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $product = Product::find($id);
        $categories = DB::table('category')->distinct()->get();

        return view('product.show', compact('categories'),compact('product', 'product_id'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $product = Product::find($id);
        $categories = DB::table('category')->distinct()->get();

        return view('product.edit', compact('categories'),compact('product', 'product_id'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'product_name' => 'required|max:255',
            'product_total_quantity' => 'required',
            'product_sku' => 'required|max:255',
            'price' => 'required',
            'category_id' => 'required|exists:category',
            'img_url' => 'required',
        ]);

        Product::find($id)->update($request->all());
        return redirect('/home');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Product::find($id)->delete();

        return redirect('/product');
    }
}
